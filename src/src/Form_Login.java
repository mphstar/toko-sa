    public void dataAkun(){
        try {
            Connection conn = auth.Koneksi.configDB();
            String sql = "SELECT akun.username, akun.password, detail_akun.nama,"
                    + " detail_akun.email, detail_akun.nohp, detail_akun.jenis_kelamin,"
                    + " detail_akun.alamat, roles.jenis_roles FROM akun JOIN detail_akun "
                    + "ON akun.username = detail_akun.username JOIN roles ON detail_akun.id_roles = roles.id_roles WHERE detail_akun.username = '"+txt_username.getText()+"'";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if(rs.next()){
    
                Dashboard.txt_username.setText(rs.getString("username"));
                Dashboard.txt_email.setText(rs.getString("email"));
                Dashboard.txt_password.setText(rs.getString("password"));
                Dashboard.txt_alamat.setText(rs.getString("alamat"));
                Dashboard.txt_nama.setText(rs.getString("nama"));
                Dashboard.txt_nohp.setText(rs.getString("nohp"));
                Dashboard.nama.setText(rs.getString("nama"));
                if(rs.getString("jenis_kelamin").toString().equals("Laki laki")){
                    
                    Dashboard.setJenisKelamin = "Laki laki";
                    Dashboard.imgProfile = "/Images/circled_user_male_skin_type_5_100px.png";
//                            Dashboard.iconfoto.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/circled_user_male_skin_type_5_100px.png")));
//                            Dashboard.lakilaki.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_enable_30px.png")));
//                            Dashboard.perempuan.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_disable_26px.png")));
//                      
                    
                } else if (rs.getString("jenis_kelamin").toString().equals("Perempuan")) {
                    
                    
                    Dashboard.setJenisKelamin = "Perempuan";
                        Dashboard.imgProfile = "/Images/circled_user_female_skin_type_4_100px.png";
//                            Dashboard.perempuan.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_enable_30px.png")));
//                            Dashboard.lakilaki.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_disable_26px.png")));
//                            Dashboard.iconfoto.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/circled_user_female_skin_type_4_100px.png")));
//                    
                } else {
                    System.out.println("error");
                }
                // check roles
                if(rs.getString("jenis_roles").equals("admin")){
                    Riwayat.panel_hapus.setVisible(true);
        
                    Riwayat.nilai_opsi = 0;
                    Dashboard.checkRoles = 0;
                    
                } else {
                    Riwayat.panel_hapus.setVisible(false);
       
                    Dashboard.checkRoles = 1;
                    Riwayat.nilai_opsi = 1;
                }
                
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }
    
 
