    private void login(){
        if ( txt_username.getText().equals("")|| txt_password.getText().equals("")){
            new PopUpMessages(this, true, "Informasi", "Username dan Password wajib diisi", 0).setVisible(true);
        } else {
            try {
                String sql = "SELECT * FROM akun WHERE username='"+txt_username.getText()
                            +"'AND password='"+txt_password.getText()+"'";
                java.sql.Connection conn= auth.Koneksi.configDB();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
              
                java.sql.ResultSet rs = pst.executeQuery(sql);
                if (rs.next()){
                    if (txt_username.getText().equals(rs.getString("username"))
                        && txt_password.getText().equals(rs.getString("password"))){
                        dataAkun();
                        PopUpMessages sukses = new PopUpMessages(this, true, "Berhasil", "Login Berhasil", 1);
                        
                        sukses.setVisible(true);
                        
                        //this.setVisible(false);
                        this.dispose();
                        
                        Dashboard menu = new Dashboard();
                        
                        menu.setVisible(true);
                        
                        
                        Detail_Users.saveUser = txt_username.getText();
                        String usernamenya = txt_username.getText();
                        Dashboard.username.setText(usernamenya);
                        Pembelian_Barang.username = usernamenya;
                    } 
                } else {
                        new PopUpMessages(this, true, "Gagal", "Username atau Password salah", 2).setVisible(true);
                        txt_password.setText("");
                    }
            } catch (SQLException ex) {
                ex.getMessage();
            }
        }
    }
    
