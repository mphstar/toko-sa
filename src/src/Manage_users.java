    private void tabel_usersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_usersMouseClicked
        int baris = tabel_users.rowAtPoint(evt.getPoint());
        String user = tabel_users.getValueAt(baris,0).toString();
        String pw = tabel_users.getValueAt(baris, 1).toString();
        String nama = tabel_users.getValueAt(baris, 2).toString();
        String email = tabel_users.getValueAt(baris, 3).toString();
        String nohp = tabel_users.getValueAt(baris, 4).toString();
        String alamat = tabel_users.getValueAt(baris, 6).toString();
        String roles = tabel_users.getValueAt(baris, 7).toString();
        
        String jenis_kelamin;
        

        if (tabel_users.getValueAt(baris, 5).toString().equals("Laki laki")){
            jenis_kelamin = "Laki laki";
//            Detail_Users.jCheckBox2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_enable_30px.png")));
//            DetaiDetail_Usersl_Users.jCheckBox1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/tick_box_disable_26px.png")));
// 
        }else{
            jenis_kelamin = "Perempuan";
//         p
        }
        
        String pilihuser = tabel_users.getValueAt(baris,0).toString();

        Detail_Users detail = new Detail_Users(this, true);
        detail.show(user, pw, nama, email, roles, alamat, nohp, jenis_kelamin, pilihuser);
        detail.select(1);
        detail.setVisible(true);
        
        load_table();
        
        
    }//GEN-LAST:event_tabel_usersMouseClicked

  
    
    
    
