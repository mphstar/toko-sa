    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        tabel_users.getTableHeader().setDefaultRenderer(new CustomTableHeader());
 
        tabel_users.setBorder(new EmptyBorder(10, 5, 10, 5));
        tabel_users.setDefaultEditor(Object.class, null);
        
        model.addColumn("Username");
        model.addColumn("Password");
        model.addColumn("Nama");
        model.addColumn("Email");
        model.addColumn("NoHp");
        model.addColumn("Jenis_Kelamin");
        model.addColumn("Alamat");
        model.addColumn("Jenis_roles");
            try {
          
            String stl = "SELECT akun.username, akun.password, detail_akun.nama, "
                    + "detail_akun.email, detail_akun.nohp, detail_akun.jenis_kelamin, "
                    + "detail_akun.alamat, roles.jenis_roles from detail_akun "
                    + "JOIN roles ON detail_akun.id_roles = roles.id_roles "
                    + "JOIN akun ON akun.username = detail_akun.username "
                    + "WHERE detail_akun.username LIKE '%"+txt_search.getText()+"%' "
                    + "OR detail_akun.nama LIKE '%"+txt_search.getText()+"%'";
            java.sql.Connection conn=auth.Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(stl);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),
                    res.getString(2),res.getString(3),res.getString(4),
                    res.getString(5),res.getString(6),res.getString(7),
                    res.getString(8)});
            }
            tabel_users.setModel(model);
            for (int i = 0; i <= 7; i++) {
                tabel_users.getColumnModel().getColumn(i).setCellRenderer(new CustomTable());
            }
        }catch (Exception e){   
            System.out.println(e);
        }
    }
   
 
